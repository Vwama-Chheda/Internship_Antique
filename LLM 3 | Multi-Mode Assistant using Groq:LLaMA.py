def run_llm_program():
    """
    Interactive loop:
    1. Ask the user for input text
    2. Ask which mode they want
    3. Build the prompt and call the LLM
    4. Display the result
    """

    print("=" * 50)
    print("       🤖 LLM Multi-Mode Assistant")
    print("=" * 50)

    # Step 1: Get user input
    user_input = input("\n📝 Enter your text or query:\n> ")

    # Step 2: Pick a mode
    print("\n🎛️  Choose a mode:")
    print("  1. Summarization")
    print("  2. Explanation")
    print("  3. Insight / Analysis")

    mode_choice = input("\nEnter 1, 2, or 3: ").strip()

    # Map number to mode name
    mode_map = {
        "1": "summarization",
        "2": "explanation",
        "3": "insight"
    }

    mode = mode_map.get(mode_choice, "summarization")  # Default to summarization

    # Step 3: Build the prompt using the selected mode
    prompt = get_prompt(mode, user_input)

    print(f"\n⏳ Sending to Claude in [{mode.upper()}] mode...\n")

    # Step 4: Query the LLM and display the response
    response = query_llm(prompt)

    print("=" * 50)
    print(f"📤 Response ({mode.upper()} mode):")
    print("=" * 50)
    print(response)

# Run the program
run_llm_program()

