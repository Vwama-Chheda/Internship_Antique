# CELL 3: Define prompts for the 3 modes
# Each mode wraps the user's input with a different instruction
# This is the core of "prompt design" (Task 3)
# ============================================================

def get_prompt(mode, user_input):
    """
    Returns a mode-specific prompt string.

    Modes:
      1. summarization  - Condenses the text into key points
      2. explanation    - Explains the text in simple terms
      3. insight        - Extracts key insights or analysis
    """

    if mode == "summarization":
        # Mode 1: Ask the LLM to summarize
        return f"Summarize the following text in a few clear bullet points:\n\n{user_input}"

    elif mode == "explanation":
        # Mode 2: Ask the LLM to explain simply
        return f"Explain this in simple terms, as if talking to a beginner:\n\n{user_input}"

    elif mode == "insight":
        # Mode 3: Ask the LLM for analysis/insights
        return f"Give key insights and analysis from the following:\n\n{user_input}"

    else:
        # Fallback if an unrecognized mode is passed
        return f"Respond to the following:\n\n{user_input}"

print("✅ Prompt functions defined!")

