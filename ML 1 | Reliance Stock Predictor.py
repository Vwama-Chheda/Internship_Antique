# ── STEP 4: Define the model rules ────────────────────────────

def my_model(row):
    # Rule 1: Price above MA and rising → UP
    if row["Above_MA"] == 1 and row["Daily_Change"] > 0:
        return 1
    # Rule 2: Price below MA and falling → DOWN
    elif row["Above_MA"] == 0 and row["Daily_Change"] < 0:
        return 0
    # Rule 3: Mixed signals → follow daily change
    else:
        if row["Daily_Change"] > 0:
            return 1
        else:
            return 0

# ── STEP 5: Apply your model to every row ─────────────────────

stock["Predicted"] = stock.apply(my_model, axis=1)

result = stock[["Close", "Daily_Change", "MA3", "Above_MA", "Predicted", "Target"]].copy()
result["Correct"] = (result["Predicted"] == result["Target"])

print(result)

# ── STEP 6: Calculate accuracy ────────────────────────────────

correct     = result["Correct"].sum()       # number of correct predictions
total       = len(result)                   # total predictions made
accuracy    = (correct / total) * 100       # accuracy in %

print(f"Correct predictions : {correct} / {total}")
print(f"Accuracy            : {accuracy:.1f}%")
print()

# Count how many UP and DOWN calls were correct
up_correct   = result[(result["Target"]==1) & (result["Predicted"]==1)].shape[0]
down_correct = result[(result["Target"]==0) & (result["Predicted"]==0)].shape[0]
print(f"UP predictions correct   : {up_correct}")
