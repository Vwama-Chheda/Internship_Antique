import yfinance as yf          # downloads stock prices
import pandas as pd            # works with tables (DataFrames)
import matplotlib.pyplot as plt  # draws charts

# We are using Reliance Industries from NSE
# NSE symbol = RELIANCE.NS
# BSE symbol would be RELIANCE.BO

stock = yf.download("RELIANCE.NS", period="1mo", interval="1d")

# Keep only the closing price column
stock = stock[["Close"]].copy()

# Rename for simplicity
stock.columns = ["Close"]

# Show the data
print(stock)
print("\nTotal days:", len(stock))

# ── STEP 2: Create target column ──────────────────────────────
# If tomorrow's price is higher than today → 1 (UP)
# If tomorrow's price is lower than today  → 0 (DOWN)

stock["Tomorrow"] = stock["Close"].shift(-1)        # next day's price
stock["Target"]   = (stock["Tomorrow"] > stock["Close"]).astype(int)

# ── STEP 3: Create features ───────────────────────────────────

# Feature 1: Daily % change (did price go up or down TODAY?)
stock["Daily_Change"] = stock["Close"].pct_change() * 100

# Feature 2: 3-day moving average (smooth average of last 3 days)
stock["MA3"] = stock["Close"].rolling(window=3).mean()

# Feature 3: Is today's price above the moving average?
# 1 = above (bullish), 0 = below (bearish)
stock["Above_MA"] = (stock["Close"] > stock["MA3"]).astype(int)

# Drop rows that have missing values (first few rows won't have MA3)
